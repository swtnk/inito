"""Per-instance memory footprint: handwritten vs @Data vs dataclasses vs attrs.

tracemalloc-based, run as a script (repeated instantiation inside a
pytest-benchmark round would confuse the allocator's peak-tracking). Run
directly:

    python benchmarks/memory_profile.py
"""

from __future__ import annotations

import gc
import tracemalloc
from dataclasses import dataclass as std_dataclass

import attrs

from inito import Data

INSTANCE_COUNT = 100_000


class HandwrittenPoint:
    """Hand-written baseline."""

    def __init__(self, x: int, y: int, label: str = "origin") -> None:
        self.x = x
        self.y = y
        self.label = label


@Data
class InitoPoint:
    """Same shape, generated by @Data."""

    x: int
    y: int
    label: str = "origin"


@std_dataclass
class DataclassPoint:
    """Same shape, via the stdlib dataclasses module."""

    x: int
    y: int
    label: str = "origin"


@attrs.define
class AttrsPoint:
    """Same shape, via attrs (slotted by default under attrs.define)."""

    x: int
    y: int
    label: str = "origin"


def _measure(cls: type) -> float:
    gc.collect()
    tracemalloc.start()
    instances = [cls(1, 2, "here") for _ in range(INSTANCE_COUNT)]
    _current, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()
    del instances
    return peak / INSTANCE_COUNT


def main() -> None:
    """Print per-instance memory footprint for each flavor."""
    for name, cls in (
        ("handwritten", HandwrittenPoint),
        ("inito", InitoPoint),
        ("dataclass", DataclassPoint),
        ("attrs (slotted)", AttrsPoint),
    ):
        bytes_per_instance = _measure(cls)
        print(f"{name:>16}: {bytes_per_instance:6.1f} bytes/instance")


if __name__ == "__main__":
    main()
