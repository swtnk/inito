"""Shared class definitions compared across the benchmark suite.

Every flavor below declares the same three fields (two required, one
defaulted) so construction/repr/eq/hash/attribute-access costs are
comparable apples-to-apples.
"""

from __future__ import annotations

from dataclasses import dataclass as std_dataclass

import attrs
import pytest

from inito import Data, builder


class HandwrittenPoint:
    """A hand-written equivalent, the performance baseline."""

    def __init__(self, x: int, y: int, label: str = "origin") -> None:
        self.x = x
        self.y = y
        self.label = label

    def __repr__(self) -> str:
        return f"HandwrittenPoint(x={self.x!r}, y={self.y!r}, label={self.label!r})"

    def __eq__(self, other: object) -> bool:
        if other.__class__ is not self.__class__:
            return NotImplemented
        return (self.x, self.y, self.label) == (other.x, other.y, other.label)

    def __hash__(self) -> int:
        return hash((self.x, self.y, self.label))


@Data
class InitoPoint:
    """Same shape, generated by @Data."""

    x: int
    y: int
    label: str = "origin"


@builder
@Data
class InitoBuilderPoint:
    """Same shape, with @Builder added for the builder-specific benchmarks."""

    x: int
    y: int
    label: str = "origin"


@std_dataclass(unsafe_hash=True)
class DataclassPoint:
    """Same shape, via the stdlib dataclasses module.

    unsafe_hash=True so it's hashable like the other flavors - @Data always
    attaches __hash__ regardless of mutability (Lombok parity), whereas
    dataclasses/attrs default to unhashable for mutable+eq classes.
    """

    x: int
    y: int
    label: str = "origin"


@attrs.define(hash=True)
class AttrsPoint:
    """Same shape, via attrs (hash=True for the same reason as above)."""

    x: int
    y: int
    label: str = "origin"


CLASS_FLAVORS = {
    "handwritten": HandwrittenPoint,
    "inito": InitoPoint,
    "dataclass": DataclassPoint,
    "attrs": AttrsPoint,
}


@pytest.fixture(params=sorted(CLASS_FLAVORS), ids=sorted(CLASS_FLAVORS))
def point_class(request: pytest.FixtureRequest) -> type:
    """Parametrized fixture yielding each comparable Point flavor in turn."""
    return CLASS_FLAVORS[request.param]


@pytest.fixture
def builder_point_class() -> type:
    """The @Builder-equipped flavor, for builder-specific benchmarks."""
    return InitoBuilderPoint


def _define_handwritten() -> type:
    class Point:
        def __init__(self, x: int, y: int, label: str = "origin") -> None:
            self.x = x
            self.y = y
            self.label = label

    return Point


def _define_inito() -> type:
    @Data
    class Point:
        x: int
        y: int
        label: str = "origin"

    return Point


def _define_dataclass() -> type:
    @std_dataclass
    class Point:
        x: int
        y: int
        label: str = "origin"

    return Point


def _define_attrs() -> type:
    @attrs.define
    class Point:
        x: int
        y: int
        label: str = "origin"

    return Point


CLASS_FACTORIES = {
    "handwritten": _define_handwritten,
    "inito": _define_inito,
    "dataclass": _define_dataclass,
    "attrs": _define_attrs,
}


@pytest.fixture(params=sorted(CLASS_FACTORIES), ids=sorted(CLASS_FACTORIES))
def point_class_factory(request: pytest.FixtureRequest):
    """Parametrized fixture yielding a fresh-class-defining callable per flavor.

    Used for decoration-time benchmarks, where each round must define a brand
    new class - reusing an already-decorated class would measure the cached
    metadata fast path, not real decoration cost.
    """
    return CLASS_FACTORIES[request.param]
