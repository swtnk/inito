"""pyperf-based construction benchmark: handwritten vs @Data vs dataclasses vs attrs.

pytest-benchmark (see the other files in this directory) is convenient for
CI/local regression tracking; pyperf trades that convenience for more
statistically rigorous, process-isolated measurements - closer to what you'd
cite in a performance comparison. Run directly:

    python benchmarks/pyperf_suite.py
"""

from __future__ import annotations

from dataclasses import dataclass as std_dataclass

import attrs
import pyperf

from inito import Data


class HandwrittenPoint:
    """Hand-written baseline."""

    def __init__(self, x: int, y: int, label: str = "origin") -> None:
        self.x = x
        self.y = y
        self.label = label


@Data
class InitoPoint:
    """Same shape, generated by @Data."""

    x: int
    y: int
    label: str = "origin"


@std_dataclass
class DataclassPoint:
    """Same shape, via the stdlib dataclasses module."""

    x: int
    y: int
    label: str = "origin"


@attrs.define
class AttrsPoint:
    """Same shape, via attrs."""

    x: int
    y: int
    label: str = "origin"


def main() -> None:
    """Run the pyperf construction comparison across all four flavors."""
    runner = pyperf.Runner()
    runner.bench_func("construction-handwritten", lambda: HandwrittenPoint(1, 2, "here"))
    runner.bench_func("construction-inito", lambda: InitoPoint(1, 2, "here"))
    runner.bench_func("construction-dataclass", lambda: DataclassPoint(1, 2, "here"))
    runner.bench_func("construction-attrs", lambda: AttrsPoint(1, 2, "here"))


if __name__ == "__main__":
    main()
